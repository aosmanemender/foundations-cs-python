total_grade = float( input("Enter your total grade: ") )

if total_grade > 89:
  print("Your grade is an A")
elif total_grade > 79:
  print("Your grade is an B")
elif total_grade > 69:
  print("Your grade is an C")
elif total_grade > 59:
  print("Your grade is an D")
else:
  print("Your grade is an F")
